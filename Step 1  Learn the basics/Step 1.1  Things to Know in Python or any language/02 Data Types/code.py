# String
x = "CodeAndGeek"
print(x)

# Int
x = 50
print(x)

# Float
x = 60.5
print(x)

# Complex
x = 3j
print(x)

# List
x = ['karan', 10, 20, 3j, 10.2, True]
print(x)

# Tuple
x = ('karan', 'karan', 'siddhu', 10, 20, 3j, 10.5, False)
print(x)

# Range
x = range(10)
print(x)

# Dict
x = {'key': 'value', 'name': 'karan', 'age': 20}
print(x)

# Set
x = {"karan", "siddhu", "karan", 10, 10, 20, 20, 10.4, True}
print(x)

# Frozenset
x = frozenset({"karan", "siddhu", "karan"})
print(x)

# Bool
x = False
print(x)

# Byte
x = b"karan"
print(x)

# Byte Array
x = bytearray(4)
print(x)

# Memory View
x = memoryview(bytes(6))
print(x)

# NoneType
x = None
print(x)
